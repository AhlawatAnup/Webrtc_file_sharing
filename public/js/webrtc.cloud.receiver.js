/*

WEBRTC RECIVER STRUCT
{
  socket: share.socket,
  videoEl: video_elemnet,
  stream_type: "share",
  topic_name: "SHARE_VIDEO",
  debug: true
}


*/

class iDronam_WebRTC_Recv extends EventTarget {
  constructor(conf) {
    super();
    this.socket = conf.socket;
    this.videoEl = conf.videoEl;
    this.stream_type = conf.stream_type;
    this.topic_name = conf.topic_name || "VIDEO_DATA";

    this.debug = conf.debug || false;

    this.pc = null;
    this.pc_state = false;
    this.restartTimeout = null;

    this.call_id = null;
    this.caller_id = "Someone";
    this.offer_receive_timeout = null;

    this._log("Initializing receiver:", this.stream_type);

    // WEBRTC STATS
    this.stats_interval = null;
    this.last_bytes_received = 0;
    this.last_stats_timestamp = 0;
    this.viewing_start_timestamp = null;

    this.isDestroyed = false;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END

    // INITIATORS
    this._initSignalingListeners();
    this._createPeerConnection();
  }

  // DEBUG HELPERS
  _log(...args) {
    if (this.debug) console.log("[WebRTC-Recv]", ...args);
  }

  _warn(...args) {
    if (this.debug) console.warn("[WebRTC-Recv]", ...args);
  }

  _error(...args) {
    if (this.debug) console.error("[WebRTC-Recv]", ...args);
  }

  // CREATE PEER CONNECTION
  _createPeerConnection() {
    this._log("Creating PeerConnection:", this.stream_type);

    const iceConfig = {
      iceServers: [
        { urls: "stun:stun.l.google.com:19302" },
        { urls: "stun:stun1.l.google.com:19302" },
        {
          urls: "turn:coturn.idronam.com:3478",
          username: "stream",
          credential: "strongpassword"
        },
        {
          urls: "turn:coturn.idronam.com:3478?transport=tcp",
          username: "stream",
          credential: "strongpassword"
        }
      ]
    };

    this.pc = new RTCPeerConnection(iceConfig);
    this.pc.addTransceiver("video", { direction: "recvonly" });

    // TRACK RECEIVED
    this.pc.ontrack = evt => {
      this._log("Video track received");
      this._log("Video tracks : ", evt.streams);

      if (evt.streams && evt.streams[0]) {
        this.videoEl.srcObject = evt.streams[0];
      }
    };

    // ICE CANDIDATE
    this.pc.onicecandidate = event => {
      if (event.candidate) {
        this._log("ICE candidate generated");

        this.socket.emit(this.topic_name, {
          type: "ice_candidates",
          candidate: event.candidate,
          call_id: this.call_id,
          stream_type: this.stream_type
        });
      } else {
        this._log("ICE gathering completed");
      }
    };

    this.pc.onconnectionstatechange = () => {
      this._log("Connection State:", this.pc.connectionState);

      switch (this.pc.connectionState) {
        case "connected":
          this._log("Stream connected");
          this.pc_state = true;
          this.viewing_start_timestamp = Date.now();
          this._start_stream_stats();
          this._connection_progress(100);
          break;

        case "disconnected":
          this._warn("Connection disconnected");
          this.pc_state = false;
          this._stop_stream_stats();
          this._restartConnection();
          this._connection_progress(0);
          break;

        case "failed":
          this._error("Connection failed");
          this.pc_state = false;
          this._restartConnection();
          this._connection_progress(0);
          break;

        case "closed":
          this._warn("Connection closed");
          this.pc_state = false;
          this.viewing_start_timestamp = null;
          this._stop_stream_stats();
          this._restartConnection();
          this._connection_progress(0);
          break;
      }
    };

    this.call_id = crypto.randomUUID();
    this._log("Generated Call ID:", this.call_id);

    this._createAndSendOffer();
  }

  async _createAndSendOffer() {
    this._log("Creating offer");

    const offer = await this.pc.createOffer();

    await this.pc.setLocalDescription(offer);

    this._log("Sending offer");

    const offer_data = {
      type: "offer",
      call_id: this.call_id,
      offer: offer,
      stream_type: this.stream_type
    };

    this.socket.emit(this.topic_name, offer_data);

    this.is_answer_received(offer_data);
  }

  // SIGNALING LISTENER
  _initSignalingListeners() {
    this._log("Listening for signaling:", this.topic_name);

    this.socket.on(this.topic_name, async data => {
      this._log("Got Event");

      if (data.stream_type == this.stream_type) {
        this._log("Signaling received:", data.type);

        this.video_data_parser(data);
      }
    });
  }

  // SIGNALING PARSER
  async video_data_parser(video_data) {
    switch (video_data.type) {
      case "offer":
        this._log("Offer received");

        if (this.call_id && this.pc_state) {
          this._warn("Another viewer already watching");
          break;
        }

        this.call_id = video_data.call_id;

        this.parse_offer(video_data);

        break;

      case "answer":
        this._log("In Answer recived : ", video_data.call_id, this.call_id);

        if (video_data.call_id != this.call_id) break;

        this._log("Answer received");

        clearTimeout(this.offer_receive_timeout);

        this._connection_progress(30);

        // GENERATED BY AI -- START
        if (this.pc.signalingState !== "have-local-offer") {
          this._warn(
            "Skipping remote answer because signaling state is:",
            this.pc.signalingState
          );

          break;
        }

        await this.pc.setRemoteDescription(video_data.answer);

        this.remoteDescriptionSet = true;

        // PROCESS QUEUED ICE CANDIDATES
        while (this.pendingIceCandidates.length > 0) {
          const candidate = this.pendingIceCandidates.shift();

          try {
            await this.pc.addIceCandidate(candidate);

            this._log("Queued ICE candidate added");
          } catch (err) {
            this._error("Failed to add queued ICE candidate:", err);
          }
        }
        // GENERATED BY AI -- END

        break;

      case "ice_candidates":
        if (video_data.call_id !== this.call_id || !video_data.candidate)
          return;

        this._log("ICE candidate received");

        this._connection_progress(50);

        // GENERATED BY AI -- START
        if (!this.remoteDescriptionSet) {
          this._log("Queueing ICE candidate until remote description is set");

          this.pendingIceCandidates.push(video_data.candidate);

          return;
        }

        try {
          await this.pc.addIceCandidate(video_data.candidate);

          this._log("ICE candidate added");
        } catch (err) {
          this._error("Failed to add ICE candidate:", err);
        }
        // GENERATED BY AI -- END

        break;
    }
  }

  _restartConnection() {
    if (this.isDestroyed) return;

    this._warn("Restarting connection");

    clearTimeout(this.offer_receive_timeout);

    if (this.pc) this.close();

    this._createPeerConnection();
  }

  close() {
    this._log("Closing receiver connection");

    this._connection_progress(0);

    this.pc.close();

    this.pc = null;

    this.call_id = null;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END
  }

  // OFFER RETRY
  is_answer_received(offer_data) {
    if (this.pc_state) return;

    this._warn("Waiting for answer...");

    this.offer_receive_timeout = setTimeout(() => {
      this._warn("No answer received, retrying offer");

      this.socket.emit(this.topic_name, offer_data);

      this.is_answer_received(offer_data);
    }, 10000);
  }

  // START  STAT COLLECTOR
  _start_stream_stats() {
    if (this.stats_interval) return;

    this.stats_interval = setInterval(async () => {
      if (!this.pc || !this.pc_state) return;

      const stats = await this.pc.getStats();

      let packets_received = 0;
      let packets_lost = 0;
      let bytes_received = 0;
      let fps = 0;
      let rtt = 0;

      stats.forEach(report => {
        if (report.type === "inbound-rtp" && report.kind === "video") {
          packets_received = report.packetsReceived || 0;
          packets_lost = report.packetsLost || 0;
          bytes_received = report.bytesReceived || 0;

          if (report.framesPerSecond) fps = report.framesPerSecond;
        }

        if (report.type === "candidate-pair" && report.state === "succeeded") {
          rtt = report.currentRoundTripTime || 0;
        }
      });

      // bitrate calculation
      let bitrate = 0;

      if (this.last_bytes_received && this.last_stats_timestamp) {
        const bytes_diff = bytes_received - this.last_bytes_received;

        const time_diff = (Date.now() - this.last_stats_timestamp) / 1000;

        bitrate = (bytes_diff * 8) / time_diff;
      }

      this.last_bytes_received = bytes_received;
      this.last_stats_timestamp = Date.now();

      const packet_loss_percent =
        packets_received + packets_lost > 0
          ? (packets_lost / (packets_received + packets_lost)) * 100
          : 0;

      const viewing_time_seconds = this.viewing_start_timestamp
        ? Math.floor((Date.now() - this.viewing_start_timestamp) / 1000)
        : 0;

      this._log("------ webrtc_stream_stats ------");

      const webrtc_stats = {
        type: "stats",
        status: "active",
        stream_type: this.stream_type,
        received_in_KB: (bytes_received / 1024).toFixed(2),
        bitrate_kbps: (bitrate / 1000).toFixed(1),
        fps: fps,
        packets_received: packets_received,
        packets_lost: packets_lost,
        packet_loss_percent: packet_loss_percent.toFixed(2),
        rtt_ms: (rtt * 1000).toFixed(1),
        viewing_time_seconds: viewing_time_seconds
      };

      this._log("WebRTC Stats:", webrtc_stats);

      this._log("-------------------------------");

      // SEND TO SERVER
      this.socket.emit(this.topic_name, webrtc_stats);

      // THROW AND EVENT FOR STATS
      this.dispatchEvent(
        new CustomEvent("STATS", {
          detail: webrtc_stats
        })
      );
    }, 3000);
  }

  // STOP STAT COLLECTOR
  _stop_stream_stats() {
    if (this.stats_interval) {
      const stats_payload = {
        type: "stats",
        status: "inactive",
        stream_type: 0,
        received_in_KB: 0,
        viewing_time_seconds: 0
      };

      this.socket.emit(this.topic_name, stats_payload);

      clearInterval(this.stats_interval);

      this.stats_interval = null;
    }
  }

  // FUNCTION TO SET CONNECTION PROGRESS TO FIND PEAR :: IN %
  _connection_progress(progress) {
    this.dispatchEvent(
      new CustomEvent("CONN_PROGRESS", {
        detail: {
          progress
        }
      })
    );
  }

  // DESTROY FUNCTION
  destroy() {
    this._log("Destroying WebRTC Receiver");

    // Stop stats collection
    this._stop_stream_stats();

    // Clear retry timers
    if (this.offer_receive_timeout) {
      clearTimeout(this.offer_receive_timeout);
      this.offer_receive_timeout = null;
    }

    if (this.restartTimeout) {
      clearTimeout(this.restartTimeout);
      this.restartTimeout = null;
    }

    // Remove socket listener
    if (this.socket) {
      this.socket.off(this.topic_name);
    }

    // Stop video stream tracks
    if (this.videoEl && this.videoEl.srcObject) {
      this.videoEl.srcObject.getTracks().forEach(track => track.stop());

      this.videoEl.srcObject = null;
    }

    // Close peer connection
    if (this.pc) {
      try {
        this.pc.ontrack = null;
        this.pc.onicecandidate = null;
        this.pc.onconnectionstatechange = null;

        this.pc.close();
      } catch (e) {
        this._warn("PeerConnection close error:", e);
      }
    }

    // Reset variables
    this.pc = null;
    this.pc_state = false;
    this.call_id = null;
    this.viewing_start_timestamp = null;
    this.last_bytes_received = 0;
    this.last_stats_timestamp = 0;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END

    this._log("WebRTC Receiver destroyed");

    this.isDestroyed = true;
  }
}
export { iDronam_WebRTC_Recv };
