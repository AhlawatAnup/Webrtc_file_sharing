// STREAM TO CLOUD

/*
 CONFIG FILE STRUCT
 conf{
  socket: vehicle.socket,
  stream: document.getElementById(iframe_id).captureStream(),
  stream_type: "main",
  topic_name: "VIDEO_DATA"
  }
*/

class iDronam_WebRTC_Sender {
  constructor(conf) {
    this.conf = conf;
    this.socket = conf.socket;
    this.stream = conf.stream;
    this.stream_type = conf.stream_type;
    this.topic_name = conf.topic_name || "VIDEO_DATA";

    this.debug = conf.debug || false;

    this.pc = null;
    this.pc_state = false;
    this.restartTimeout = null;

    this.call_id = null;
    this.caller_id = "someone";
    this.isDestroyed = false;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END

    this._log("Initializing sender:", this.stream_type);

    this._initSignalingListeners();
    this._createPeerConnection();
  }

  // DEBUG LOGGER
  _log(...args) {
    if (this.debug) console.log("[WebRTC-Sender]", ...args);
  }

  _warn(...args) {
    if (this.debug) console.warn("[WebRTC-Sender]", ...args);
  }

  _error(...args) {
    if (this.debug) console.error("[WebRTC-Sender]", ...args);
  }

  // CREATE PEER CONNECTION
  _createPeerConnection() {
    this._log("Creating PeerConnection:", this.stream_type);

    const iceConfig = {
      iceServers: [
        { urls: "stun:stun.l.google.com:19302" },
        { urls: "stun:stun1.l.google.com:19302" },
        {
          urls: "turn:coturn.idronam.com:3478",
          username: "stream",
          credential: "strongpassword"
        },
        {
          urls: "turn:coturn.idronam.com:3478?transport=tcp",
          username: "stream",
          credential: "strongpassword"
        }
      ]
    };

    this.pc = new RTCPeerConnection(iceConfig);

    // ADD STREAM
    if (this.stream) {
      this._log("Adding stream tracks");
      this._log("Tracks", this.stream.getTracks());

      this.stream.getTracks().forEach(track => {
        this._log("Track added:", track.kind);
        this.pc.addTrack(track, this.stream);
      });
    } else {
      this._warn("No stream provided");
    }

    // ICE CANDIDATE EVENT
    this.pc.onicecandidate = event => {
      if (event.candidate) {
        this._log("ICE candidate generated");

        this.socket.emit(this.topic_name, {
          type: "ice_candidates",
          call_id: this.call_id,
          candidate: event.candidate,
          stream_type: this.stream_type
        });
      } else {
        this._log("ICE gathering completed");
      }
    };

    this.pc.onconnectionstatechange = () => {
      this._log("Connection State:", this.pc.connectionState);

      switch (this.pc.connectionState) {
        case "connected":
          this._log("Viewer connected");
          this.pc_state = true;
          break;

        case "disconnected":
          this._warn("Viewer disconnected");
          this.pc_state = false;
          this._restartConnection();
          break;

        case "failed":
          this._error("Connection failed");
          this.pc_state = false;
          this._restartConnection();
          break;

        case "closed":
          this._warn("Connection closed");
          this.pc_state = false;
          this._restartConnection();
          break;
      }
    };
  }

  // INIT SIGNALING
  _initSignalingListeners() {
    this._log("Listening for signaling:", this.topic_name);

    this.socket.on(this.topic_name, async data => {
      if (data.stream_type == this.stream_type) {
        this._log("Signaling message received:", data.type);
        this.video_data_parser(data);
      }
    });
  }

  // SIGNALING PARSER
  async video_data_parser(video_data) {
    this._log("Incoming signaling:", video_data.type);

    switch (video_data.type) {
      case "offer":
        this._log("Offer received from viewer");

        if (this.call_id && this.pc_state) {
          this._warn("Another viewer already connected");
          break;
        }

        this.call_id = video_data.call_id;
        this._log("Assigned Call ID:", this.call_id);

        this.parse_offer(video_data);
        break;

      case "answer":
        if (video_data.call_id != this.call_id) break;

        this._log("Answer received");

        // GENERATED BY AI -- START
        if (this.pc.signalingState !== "have-local-offer") {
          this._warn(
            "Skipping remote answer because signaling state is:",
            this.pc.signalingState
          );

          break;
        }

        await this.pc.setRemoteDescription(video_data.answer);
        // GENERATED BY AI -- END

        break;

      case "ice_candidates":
        if (video_data.call_id !== this.call_id || !video_data.candidate)
          return;

        this._log("ICE candidate received");

        // GENERATED BY AI -- START
        if (!this.remoteDescriptionSet) {
          this._log("Queueing ICE candidate until remote description is set");

          this.pendingIceCandidates.push(video_data.candidate);

          return;
        }

        try {
          await this.pc.addIceCandidate(video_data.candidate);

          this._log("ICE candidate added");
        } catch (err) {
          this._error("Failed to add ICE candidate:", err);
        }
        // GENERATED BY AI -- END

        break;
    }
  }

  // PARSE OFFER
  async parse_offer(video_data) {
    if (!this.call_id) return;

    const active_senders = this.pc.getSenders().filter(s => s.track);

    if (active_senders.length === 0) {
      this._log("No stream tracks available");
      this._restartConnection();
      return;
    }

    this._log("Setting remote description (offer)");

    // GENERATED BY AI -- START
    await this.pc.setRemoteDescription(video_data.offer);

    this.remoteDescriptionSet = true;

    // PROCESS QUEUED ICE CANDIDATES
    while (this.pendingIceCandidates.length > 0) {
      const candidate = this.pendingIceCandidates.shift();

      try {
        await this.pc.addIceCandidate(candidate);

        this._log("Queued ICE candidate added");
      } catch (err) {
        this._error("Failed to add queued ICE candidate:", err);
      }
    }
    // GENERATED BY AI -- END

    await this._createAndSendAnswer();
  }

  // CREATE ANSWER
  async _createAndSendAnswer() {
    this._log("Creating answer");

    const answer = await this.pc.createAnswer();

    await this.pc.setLocalDescription(answer);

    this._log("Sending answer to viewer");

    this.socket.emit(this.topic_name, {
      type: "answer",
      call_id: this.call_id,
      answer: answer,
      stream_type: this.stream_type
    });
  }

  // RESTART CONNECTION
  _restartConnection() {
    if (this.isDestroyed) return;

    this._warn("Restarting streaming connection");

    this.call_id = null;
    this.pc_state = false;

    if (this.pc) this.close();

    this._createPeerConnection();
  }

  // CLOSE CONNECTION
  close() {
    this._log("Closing connection:", this.stream_type);

    this.pc.close();

    this.pc = null;

    this.call_id = null;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END
  }

  // DESTROY THE CLASS
  destroy() {
    this._log("Destroying WebRTC Sender");

    this.isDestroyed = true;

    // Clear restart timeout
    if (this.restartTimeout) {
      clearTimeout(this.restartTimeout);
      this.restartTimeout = null;
    }

    // Remove socket listener
    if (this.socket) {
      this.socket.off(this.topic_name);
    }

    // Stop stream tracks
    if (this.stream) {
      this.stream.getTracks().forEach(track => {
        this._log("Stopping track:", track.kind);
        // track.stop();
      });
    }

    // Remove senders
    if (this.pc) {
      try {
        this.pc.getSenders().forEach(sender => {
          this.pc.removeTrack(sender);
        });
      } catch (e) {
        this._warn("Sender cleanup error:", e);
      }
    }

    // Close peer connection
    if (this.pc) {
      try {
        this.pc.onicecandidate = null;
        this.pc.onconnectionstatechange = null;
        this.pc.close();
      } catch (e) {
        this._warn("PeerConnection close error:", e);
      }
    }

    // Reset variables
    this.pc = null;
    // this.stream = null;
    this.call_id = null;
    this.pc_state = false;

    // GENERATED BY AI -- START
    this.pendingIceCandidates = [];
    this.remoteDescriptionSet = false;
    // GENERATED BY AI -- END

    this._log("WebRTC Sender destroyed");
  }
}

export { iDronam_WebRTC_Sender };
